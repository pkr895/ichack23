function Marker({lat, lng, clickEvent}){
  return (
    <div
    //onClick={() => console.log("Div clicked")}
    onClick={clickEvent} 
    style={{
      backgroundColor: 'red',
      borderColor: 'white',
      borderWidth: '5px',
      borderRadius: '50%',
      width: '25px',
      height: '25px',
      display: 'inline-block',
      position: 'absolute',
      left: `${lng}px`,
      top: `${lat}px`
    }}></div>
  )
}

function PopUp({lat, lng, text}) {
  return (
    <div
    style={{
      backgroundColor: 'white',
      padding: '10px',
      position: 'absolute',
      fontSize: '16pt',
      maxWidth: '200pt',
      left: `${-100 + lng}px`,
      top: `${-100 + lat}px`,
      zIndex: 1,
    }}
    >{text}
  </div>
    )
}

